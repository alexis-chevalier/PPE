package entites;

class Dosage {
    public int codeDosage;
    public int quantiteDosage;
    public int uniteDosage;

    public int getCode() {
        return codeDosage;
    }

    public void setCode(int code) {
        this.codeDosage = code;
    }

    public int getQuantite() {
        return quantiteDosage;
    }

    public void setQuantite(int quantite) {
        this.quantiteDosage = quantite;
    }

    public int getUnite() {
        return uniteDosage;
    }

    public void setUnite(int unite) {
        this.uniteDosage = unite;
    }
    
    
}
